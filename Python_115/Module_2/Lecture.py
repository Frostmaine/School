# to identify any data type, you can use a function type()
x = 100
print("The type of x is:", type(x))
y = 100.56
print("The type of x is:", type(y))

# example of integer
m = -238947020934
print(type(m))

# example of a float number (rational number)
n = 35.239847
print(type(n))

# example of complex numbers
z = 1 + 100j
print(type(z))
